import React, { useCallback, useEffect, useState } from 'react';
import { useParams, useNavigate } from 'react-router-dom';
import { getAppointmentSummary } from '../services/api';
import '../styles/AppointmentSummary.css';

interface AppointmentSummaryProps {
    user: any;
}

const AppointmentSummary = ({ user }: AppointmentSummaryProps) => {
  const { id } = useParams();
  const navigate = useNavigate();
  const [summary, setSummary] = useState<any>(null);
  const [loading, setLoading] = useState(true);

  const loadSummary = useCallback(async () => {
    try {
      if (!id) {
        return;
      }

      setLoading(true);
      const response = await getAppointmentSummary(id);
      setSummary(response.data);
      setLoading(false);
    } catch (error) {
      console.error('Error loading summary:', error);
      setLoading(false);
    }
  }, [id]);

  useEffect(() => {
    loadSummary();
  }, [id, loadSummary]);

  const handleShare = () => {
    if (navigator.share) {
      navigator.share({
        title: 'My Appointment Summary',
        text: summary.summary
      });
    } else {
      navigator.clipboard.writeText(summary.summary);
      alert('Summary copied to clipboard!');
    }
  };

  const handlePrint = () => {
    window.print();
  };

  if (loading) {
    return (
      <div className="loading-container">
        <div className="spinner"></div>
      </div>
    );
  }

  if (!summary) {
    return (
      <div className="summary-error">
        <h2>Summary Not Available</h2>
        <p>This appointment summary could not be found.</p>
        <button className="btn btn-primary" onClick={() => navigate('/appointments')}>
          Back to Appointments
        </button>
      </div>
    );
  }

  return (
    <div className="appointment-summary-page">
      <div className="summary-container">
        <div className="summary-header">
          <button className="back-btn" onClick={() => navigate('/appointments')}>
            <svg fill="none" viewBox="0 0 24 24" stroke="currentColor">
              <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M15 19l-7-7 7-7" />
            </svg>
            Back
          </button>
          <h1>Appointment Summary</h1>
          <div className="summary-actions">
            <button className="btn btn-ghost" onClick={handleShare}>
              <svg fill="none" viewBox="0 0 24 24" stroke="currentColor">
                <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M8.684 13.342C8.886 12.938 9 12.482 9 12c0-.482-.114-.938-.316-1.342m0 2.684a3 3 0 110-2.684m0 2.684l6.632 3.316m-6.632-6l6.632-3.316m0 0a3 3 0 105.367-2.684 3 3 0 00-5.367 2.684zm0 9.316a3 3 0 105.368 2.684 3 3 0 00-5.368-2.684z" />
              </svg>
              Share
            </button>
            <button className="btn btn-primary" onClick={handlePrint}>
              <svg fill="none" viewBox="0 0 24 24" stroke="currentColor">
                <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M17 17h2a2 2 0 002-2v-4a2 2 0 00-2-2H5a2 2 0 00-2 2v4a2 2 0 002 2h2m2 4h6a2 2 0 002-2v-4a2 2 0 00-2-2H9a2 2 0 00-2 2v4a2 2 0 002 2zm8-12V5a2 2 0 00-2-2H9a2 2 0 00-2 2v4h10z" />
              </svg>
              Print
            </button>
          </div>
        </div>

        <div className="summary-card printable">
          <div className="summary-badge">
            <svg fill="none" viewBox="0 0 24 24" stroke="currentColor">
              <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M9 12l2 2 4-4m6 2a9 9 0 11-18 0 9 9 0 0118 0z" />
            </svg>
            <span>AI-Generated Summary</span>
          </div>

          <div className="summary-content">
            <h2>What the doctor found</h2>
            <div className="summary-text">
              {summary.summary}
            </div>
          </div>

          {summary.diagnosis && (
            <div className="summary-section">
              <h3>ðŸ“‹ Diagnosis</h3>
              <p>{summary.diagnosis}</p>
            </div>
          )}

          {summary.prescription && (
            <div className="summary-section">
              <h3>ðŸ’Š Prescription</h3>
              <p>{summary.prescription}</p>
            </div>
          )}

          <div className="summary-footer">
            <div className="footer-info">
              <svg fill="none" viewBox="0 0 24 24" stroke="currentColor">
                <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M13 16h-1v-4h-1m1-4h.01M21 12a9 9 0 11-18 0 9 9 0 0118 0z" />
              </svg>
              <p>
                This summary was automatically generated by AI to help you understand your visit.
                Always follow your doctor's specific instructions.
              </p>
            </div>
          </div>
        </div>

        <div className="next-steps-card">
          <h3>What to do next</h3>
          <ul className="next-steps-list">
            <li>
              <svg fill="none" viewBox="0 0 24 24" stroke="currentColor">
                <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M9 12l2 2 4-4m6 2a9 9 0 11-18 0 9 9 0 0118 0z" />
              </svg>
              <span>Follow the prescribed treatment plan</span>
            </li>
            <li>
              <svg fill="none" viewBox="0 0 24 24" stroke="currentColor">
                <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M9 12l2 2 4-4m6 2a9 9 0 11-18 0 9 9 0 0118 0z" />
              </svg>
              <span>Take medications as directed</span>
            </li>
            <li>
              <svg fill="none" viewBox="0 0 24 24" stroke="currentColor">
                <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M9 12l2 2 4-4m6 2a9 9 0 11-18 0 9 9 0 0118 0z" />
              </svg>
              <span>Schedule follow-up if recommended</span>
            </li>
            <li>
              <svg fill="none" viewBox="0 0 24 24" stroke="currentColor">
                <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M9 12l2 2 4-4m6 2a9 9 0 11-18 0 9 9 0 0118 0z" />
              </svg>
              <span>Contact your doctor if symptoms worsen</span>
            </li>
          </ul>
        </div>
      </div>
    </div>
  );
};

export default AppointmentSummary;